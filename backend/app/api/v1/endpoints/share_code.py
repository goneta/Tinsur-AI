router = APIRouter()

def generate_alphanumeric_code(length=6):
    """Generate a random alphanumeric code."""
    return ''.join(random.choices(string.ascii_uppercase + string.digits, k=length))

def generate_qr_code_base64(data: str) -> str:
    """Generate a QR code and return as base64 string."""
    qr = qrcode.QRCode(
        version=1,
        error_correction=qrcode.constants.ERROR_CORRECT_L,
        box_size=10,
        border=4,
    )
    qr.add_data(data)
    qr.make(fit=True)

    img = qr.make_image(fill_color="black", back_color="white")
    
    buffered = io.BytesIO()
    img.save(buffered, format="PNG")
    img_str = base64.b64encode(buffered.getvalue()).decode("utf-8")
    return f"data:image/png;base64,{img_str}"

@router.post("/", response_model=ShareCodeSchema)
def create_share_code(
    *,
    db: Session = Depends(get_db),
    share_code_in: ShareCodeCreate,
    current_user: User = Depends(get_current_active_user),
) -> Any:
    """
    Generate a new share code.
    """
    # Generate unique code
    code = generate_alphanumeric_code()
    
    # Ensure uniqueness (simple check)
    while db.query(ShareCode).filter(ShareCode.code == code).first():
        code = generate_alphanumeric_code()
    
    db_obj = ShareCode(
        code=code,
        creator_id=current_user.id,
        share_type=share_code_in.share_type,
        recipient_ids=share_code_in.recipient_ids,
        status="active"
    )
    db.add(db_obj)
    db.commit()
    db.refresh(db_obj)
    
    # Generate QR code for response
    # The QR code contains the code value. In a real app, it might contain a deep link URL.
    # For now, per requirements: "The QR code can be scanned (mobile app) to access or download shared documents."
    # We will encode the code itself or a JSON payload. Let's encode the code string.
    qr_code = generate_qr_code_base64(code)
    
    result = ShareCodeSchema.model_validate(db_obj)
    result.qr_code_base64 = qr_code
    return result

@router.get("/", response_model=List[ShareCodeSchema])
def read_share_codes(
    db: Session = Depends(get_db),
    skip: int = 0,
    limit: int = 100,
    current_user: User = Depends(get_current_active_user),
) -> Any:
    """
    Retrieve share codes generated by the current user.
    """
    codes = (
        db.query(ShareCode)
        .filter(ShareCode.creator_id == current_user.id)
        .filter(ShareCode.status == "active") # Only show active ones? Or all? Let's show active.
        .offset(skip)
        .limit(limit)
        .all()
    )
    
    # Populate QR codes for each
    results = []
    for code in codes:
        schema = ShareCodeSchema.model_validate(code)
        schema.qr_code_base64 = generate_qr_code_base64(code.code)
        results.append(schema)
        
    return results

@router.delete("/{id}", response_model=ShareCodeSchema)
def revoke_share_code(
    *,
    db: Session = Depends(get_db),
    id: uuid.UUID,
    current_user: User = Depends(get_current_active_user),
) -> Any:
    """
    Revoke a share code.
    """
    share_code = db.query(ShareCode).filter(ShareCode.id == id).first()
    if not share_code:
        raise HTTPException(status_code=404, detail="Share code not found")
    if share_code.creator_id != current_user.id:
        raise HTTPException(status_code=403, detail="Not enough permissions")
        
    share_code.status = "revoked"
    db.add(share_code)
    db.commit()
    db.refresh(share_code)
    return share_code

